package com.example.helldiversbuildhub;

public class Reward {
    private int type;
    private long id32;
    private int amount;

    public Reward() { }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public long getId32() {
        return id32;
    }

    public void setId32(long id32) {
        this.id32 = id32;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
